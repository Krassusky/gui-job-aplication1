def _prune_to_en_structure(tree: dict, en: dict) -> dict:
    """Keep only keys present in the English canonical structure."""
    out: dict = {}
    for key, en_val in en.items():
        if key not in tree:
            continue
        val = tree[key]
        if isinstance(en_val, dict):
            if isinstance(val, dict):
                pruned = _prune_to_en_structure(val, en_val)
                if pruned:
                    out[key] = pruned
        else:
            out[key] = val
    return out


def build_pt(en: dict, es: dict, pt_old: dict) -> dict:
    result = deepcopy(en)
    # Spanish base for bulk keys, then quality PT overrides (skip pt_old — reintroduces mixed strings).
    es_pt = deepcopy(es)
    _translate_tree(es_pt, es_to_pt)
    deep_merge(result, es_pt)
    deep_merge(result, _overrides_tree(PT_OVERRIDES))
    deep_merge(result, _overrides_tree(WORKFLOW_PT))
    deep_merge(result, _overrides_tree(HELP_PT))
    return _prune_to_en_structure(result, en)


def build_es(en: dict, es_old: dict) -> dict:
    result = deepcopy(en)
    deep_merge(result, es_old)
    deep_merge(result, _overrides_tree(ES_OVERRIDES))
    deep_merge(result, _overrides_tree(WORKFLOW_ES))
    deep_merge(result, _overrides_tree(HELP_ES))
    deep_merge(result, _overrides_tree(REUSE_ES))
    deep_merge(result, _overrides_tree(SETTINGS_ES_EXTRA))
    deep_merge(result, _overrides_tree(UPDATES_ES))
    return _prune_to_en_structure(result, en)


def _overrides_tree(flat: dict[str, str]) -> dict:
    return unflatten(flat)


def _translate_tree(node: object, fn) -> None:
    if isinstance(node, dict):
        for key, value in node.items():
            if isinstance(value, dict):
                _translate_tree(value, fn)
            elif isinstance(value, str):
                node[key] = fn(value)


